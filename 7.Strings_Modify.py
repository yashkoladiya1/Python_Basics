# Upper Case
a = "Hello, World!"
print("Upper Case----->",a.upper())

#Lower Case
b = "Hello, World!"
print("Lower Case---->",b.lower())

#Remove Whitespace
c = " Hello World "
print("remove space--->",c.strip())

#Replace String
d = "Hello, World!"
print("Replace String--->",d.replace("H","J"))

#Split String
e = "Hello, World!"
print("split string---->",e.split(","))

#String Concatenation

r = "Hello"
t = "World"
u = r + t
print("Concatenation---->",u)
